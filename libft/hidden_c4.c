int	hidden_c4(const char *s1, const char *s2, int s, int e)
{
	int	i;
	int	c;

	c = s;
	while (c < e)
	{
		i = -1;
		while (s2[++i])
			if (s1[c] == s2[i])
				return (c + 1);
		c++;
	}
	return (0);
}
