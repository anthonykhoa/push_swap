#include "libft.h"

int	hidden_c2(const char *s1, const char *s2, int s, int e)
{
	int	i;
	int	c;

	c = e;
	while (c > s)
	{
		i = -1;
		while (s2[++i])
			if (s1[c] == s2[i])
				return (c);
		c--;
	}
	return (0);
}
