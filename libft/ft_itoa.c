#include "libft.h"

char	*ft_itoa(int n)
{
	char	*str;
	char	*t;
	char	*u;
	int		i;

	if (!(str = (char *)malloc(30)))
		return (NULL);
	t = str;
	(n < 0 ? *t++ = '-' : 1);
	if (n > 0)
		n = -n;
	if (n <= -10)
	{
		i = 0;
		u = ft_itoa(-(n / 10));
		while (u[i])
			*t++ = u[i++];
		free(u);
	}
	*t = '0' - n % 10;
	*(t + 1) = '\0';
	return (str);
}
